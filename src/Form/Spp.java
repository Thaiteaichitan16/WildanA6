    private void simpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_simpanMouseClicked
        // TODO add your handling code here:
        String TH = th.getText();
        String NML = nml.getText();
        
        //buat query
        String sql = "Insert into spp(tahun,nominal) values('"+TH+"','"+NML+"')";
        // periksa jika data dapat ditambahkan
        ModelDatabase md = new ModelDatabase();
        if(md.rubahdata(sql)){
            Status.setText("Data Berhasil Ditambahkan");
            muatTabel();
        }else{
            Status.setText("Data Gagal Ditambahkan");
        }
        kosong();                                

    }//GEN-LAST:event_simpanMouseClicked

    private void hapusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_hapusMouseClicked
        // TODO add your handling code here:
         int no_baris = Tabel.getSelectedRow();
         String kode = Tabel.getValueAt(no_baris, 0).toString();
         
         // buat query hapus database
         String sql = "delete from spp where id_spp="+kode;
         
          ModelDatabase md = new ModelDatabase();
        if(md.rubahdata(sql)){
            Status.setText("Data Berhasil DiHapus");
            muatTabel();
        }else{
            Status.setText("Data Gagal DiHapus");
        }
        kosong();
    }//GEN-LAST:event_hapusMouseClicked

    private void editMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editMouseClicked
        // TODO add your handling code here:
        int no_baris = Tabel.getSelectedRow();
         //
         String kode = Tabel.getValueAt(no_baris, 0).toString();
         String TH = th.getText();
         String NML = nml.getText();
         
         // buat query update database
         String sql = "Update spp set tahun='"+TH+"',nominal='"+NML+"'where id_spp="+kode;
         
          ModelDatabase md = new ModelDatabase();
        if(md.rubahdata(sql)){
            Status.setText("Data Berhasil Diperbaharui");
            muatTabel();
        }else{
            Status.setText("Data Gagal Diperbaharui");
        }
        kosong();
    }//GEN-LAST:event_editMouseClicked

