     private void muatTabel(){
        DefaultTableModel model = (DefaultTableModel)Tabel.getModel();
        //kosongkan model
        model.getDataVector().removeAllElements();
        //buat query ambil data
        String sql = "select * from spp";
        //buat objek model database
        ModelDatabase md = new ModelDatabase();
        ResultSet rs = md.ambildata(sql);
        
        try {
            //isi data pada model
            while(rs.next()){
                Vector baris = new Vector();
                baris.add(rs.getInt("id_spp"));
                baris.add(rs.getInt("tahun"));
                baris.add(rs.getInt("nominal"));
                model.addRow(baris);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Spp.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        //set model pada tabel
        Tabel.setModel(model);
    }
